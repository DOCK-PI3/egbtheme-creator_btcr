def _write_xml_file(file_path: str, content: str):
    """Escribe un archivo XML con codificación UTF-8, creando directorios si es necesario."""
    os.makedirs(os.path.dirname(file_path), exist_ok=True)
    with open(file_path, "w", encoding="utf-8") as f:
        f.write(content)


