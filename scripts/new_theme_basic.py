def copy_assets_from_template(project_assets_dir: str, dest_assets_dir: str, items: list) -> list:
    """
    Copia archivos o carpetas desde una plantilla de assets.
    Retorna una lista de mensajes de error/advertencia.
    """
    messages = []
    for src_rel, dst_rel, is_dir in items:
        src_path = os.path.join(project_assets_dir, src_rel)
        dst_path = os.path.join(dest_assets_dir, dst_rel)

        if not os.path.exists(src_path):
            messages.append(f"No existe el archivo: {src_rel}")
            continue

        os.makedirs(os.path.dirname(dst_path), exist_ok=True)

        try:
            if is_dir:
                if os.path.exists(dst_path):
                    shutil.rmtree(dst_path)
                shutil.copytree(src_path, dst_path)
            else:
                shutil.copy2(src_path, dst_path)
        except Exception as e:
            messages.append(f"Error copiando {src_rel}: {e}")
    return messages
